name=str(input("Enter a name:"))
qty=int(input("Enter a quantity of item sold:"))
val=float(input("Enter a price of each item:"))
discount=float(input("Enter a discount percentage:"))
tax=float(input("Enter a tax amount:"))
amt=qty*val
discount_amt=(amt*discount)/100
sub_total=(amt-discount_amt)
tax_amt=(sub_total*tax)/100
total=(sub_total+tax_amt)
print("------------------ B-Star Mart ----------------")
print("******************* BILL ******************")
print("Name:",name)
print("Quantity sold:\t\t",qty)
print("Price per item:\t\t",val)
