print("Amount:\t\t\t\t",amt)
print("Discount:\t\t\t-",discount_amt)
print("\t\t\t\t\t------")
print("Discounted total:\t",sub_total)
print("Tax:\t\t\t\t+",tax_amt)
print("\t\t\t\t\t--------")
print("Total Amount:\t\t",total)
